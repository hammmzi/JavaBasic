public class Literal2 {
	public static void main(String[] args) {
		
		//基本データ型
		long l =123456789012L;
		boolean b = true;
		//参照型
		String str = "おはようございます。";
		//String変数に保存	　ｂはb(boolean）の値
		//右側の処理から
		String message = "b = "+b;
		//print
		System.out.println("l ＝ " + l);
		System.out.println(message);
		System.out.println(str);
		
	
	}
}
