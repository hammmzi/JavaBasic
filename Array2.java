
public class Array2 {

	public static void main(String[] args) {
		
		int[] ary = {10,15,12,8};
				
		System.out.println("配列aryの大きさは" + ary.length+"です。");

		//型名[]　配列名　＝｛要素1、要素２，要素３．．．｝；
		String[] sary= {"おはようございます。", "こんにちは！", "こんばんは:)"};
		System.out.println(sary[0]);
		System.out.println(sary[1]);
		System.out.println(sary[2]);
		System.out.println(sary.length);
	}
}
