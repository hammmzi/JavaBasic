public class Literal1 {
	public static void main(String[] args) {
		int a = 10;//10진수
		int b = 012;//8진수
		int c = 0xA;//16진수
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = " + c);

		double d = 3.14;
		//float型はｆまたはＦを付ける
		float f = 1.25f;
		System.out.println("d = " + d);
		System.out.println("f = " + f);
	}
}
